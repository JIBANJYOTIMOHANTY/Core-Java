public class Complement_oper
{
	public static void main(String args[])
	{
		boolean b = false;
		if(b)
		{	
			System.out.println("Hello.");
		}
		else if(b = false)
		{
			System.out.println("Hi.");			
		}
		else if(!b)
		{
			System.out.println("Namaste.");
		}
		else if(b = true)
		{
			System.out.println("Namaskar.");
		}
		else
		{
			System.out.println("Wadakam");
		}
	}
}